#include<stdio.h>
//stdio.h is a header file
//.h is a header file extension
//header file is a file which contains the declaration of functions, variables, macros, etc.
//include is a preprocessor directive
//preprocessor directive is a directive which is processed by the preprocessor
//preprocessor is a program which is executed before the compilation
//directives are used to instruct the preprocessor to perform certain tasks

int main()
//main is a function
//function is a block of code which is used to perform a specific task

{
    //{} is a block of code
    //block of code is a group of statements enclosed in curly braces
    //block of code is also called as function body, function definition, function implementation
    
    printf("Hello World");
    //printf is a function

    return 0;
    //return is a keyword
    //return is a statement
    //return is a statement which is used to return a value from a function
    //0 is an integer literal
    //integer literal is a sequence of digits
    //0 is a return value
    //return value is a value which is returned by a function
}

//Compiler: gcc 6.3.0
//Version: 1.0.0
//Status: Working
//Output: Hello World