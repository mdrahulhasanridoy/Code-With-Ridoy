//Question One : Write a C program to show output like below:
/*
    Hi there /\.    I hope you are doing great and practicing well.
    Now this is a simple task you have to do.   Just care for the indentation.
    Show 100% of sincerity.
*/

#include<stdio.h>
int main()
{
    //print the message
    // \\ is used to print a single backslash
    // \t is used to print a tab
    // \n is used to print a new line
    // %% is used to print a single percent sign
    printf("Hi there /\\.  I hope you are doing great and practicing well.\nNow this is a simple task you have to do.\tJust care for the indentation.\nShow 100%% of sincerity.");

    return 0;
}